#include "ft_memory.h"

#include "wrap.h"

void	*ft_memory_duplicate(const void *source, size_t size)
{
	void *const	result = wrap_malloc(size);

	if (!result)
		return (NULL);
	ft_memory_copy(result, source, size);
	return (result);
}
