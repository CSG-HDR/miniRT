#include "ft_io.h"

#include <stdint.h>

#include "ft_types.h"

t_err	ft_io_le_read_u32(int fd, uint32_t *out_u32)
{
	uint32_t	buffer;

	if (ft_io_read(fd, &buffer, 4))
		return (true);
	*out_u32 = ft_io_le_convert_u32(buffer);
	return (false);
}
