#include "ft_cstring.h"

bool	ft_cstring_ends_with_char(
	const char *self,
	char expected_end
)
{
	const size_t	index = ft_cstring_find_index_reverse(self, expected_end);

	return (index != (size_t)(-1) && self[index + 1] == '\0');
}
